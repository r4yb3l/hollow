import 'dart:convert';
import 'dart:io';

/// Writes captured bone data to `.bones.json` files and generates
/// `bones_registry.dart`.
Future<void> writeOutput(
  Map<String, Map<String, dynamic>> newCaptures,
  Map<String, Map<String, dynamic>> existing,
  String outDir,
) async {
  final dir = Directory(outDir);
  await dir.create(recursive: true);

  final all = <String, Map<String, dynamic>>{...existing, ...newCaptures};
  final names = all.keys.toList()..sort();
  final newBones = newCaptures.keys.toSet();

  stdout.writeln('\n  \x1b[2m${'─' * 50}\x1b[0m');
  stdout.writeln('  \x1b[1mWriting files\x1b[0m\n');

  for (final name in names) {
    final data = all[name]!;
    final isNew = newBones.contains(name);
    final prefix = isNew ? '\x1b[32m→\x1b[0m' : '\x1b[2m·\x1b[0m';
    final file = File('${dir.path}/$name.bones.json');
    await file.writeAsString(
      const JsonEncoder.withIndent('  ').convert(data),
    );
    stdout.writeln('  $prefix $name.bones.json');
  }

  final registryLines = <String>[
    '// Auto-generated by `dart run hollow:build` — do not edit',
    "import 'package:hollow/hollow.dart';",
    '',
  ];

  for (final name in names) {
    final varName = '_${_toIdentifier(name)}';
    final json = const JsonEncoder().convert(all[name]);
    registryLines.add('final $varName = SkeletonResult.fromJson($json);');
  }

  registryLines.addAll([
    '',
    'void registerAllBones() {',
    '  HollowRegistry.register({',
  ]);

  for (final name in names) {
    final varName = '_${_toIdentifier(name)}';
    registryLines.add("    '$name': $varName,");
  }

  registryLines.addAll([
    '  });',
    '}',
    '',
  ]);

  final registryFile = File('${dir.path}/bones_registry.dart');
  await registryFile.writeAsString(registryLines.join('\n'));

  final newCount = newBones.length;
  final totalCount = names.length;
  stdout.writeln(
    '  \x1b[32m→\x1b[0m bones_registry.dart  '
    '\x1b[2m($totalCount skeleton${totalCount != 1 ? 's' : ''})\x1b[0m',
  );
  stdout.writeln(
    '\n  \x1b[32m\x1b[1m💀 $newCount new, $totalCount total.\x1b[0m\n',
  );
  stdout.writeln(
    "  \x1b[2mAdd once to main():\x1b[0m  import '$outDir/bones_registry.dart';\n"
    "  \x1b[2m                      \x1b[0m  registerAllBones();\n",
  );
}

Future<Map<String, Map<String, dynamic>>> readExistingBones(String outDir) async {
  final dir = Directory(outDir);
  if (!await dir.exists()) return {};

  final result = <String, Map<String, dynamic>>{};
  await for (final entity in dir.list()) {
    if (entity is! File || !entity.path.endsWith('.bones.json')) continue;
    final content = await entity.readAsString();
    try {
      final json = jsonDecode(content) as Map<String, dynamic>;
      final name = json['name'] as String?;
      if (name != null) result[name] = json;
    } catch (_) {}
  }
  return result;
}

String _toIdentifier(String name) {
  final parts = name.split(RegExp(r'[^a-zA-Z0-9]+'));
  return parts.first.toLowerCase() +
      parts
          .skip(1)
          .map((p) => p.isEmpty ? '' : p[0].toUpperCase() + p.substring(1))
          .join();
}
