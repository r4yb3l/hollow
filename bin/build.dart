#!/usr/bin/env dart
import 'dart:async';
import 'dart:convert';
import 'dart:io';

const _prefix = 'HOLLOW_JSON:';

void main(List<String> args) async {
  if (args.contains('--help') || args.contains('-h')) {
    _printHelp();
    exit(0);
  }

  String? deviceId = _argValue(args, '--device');
  String outDir = _argValue(args, '--out') ?? 'lib/bones';
  final timeoutMs = int.tryParse(_argValue(args, '--timeout') ?? '') ?? 5000;

  if (deviceId == null) {
    stdout.write('\n  hollow: detecting device...');
    deviceId = await _detectDevice();
    if (deviceId == null) {
      stdout.write(' none found.\n\n');
      stderr.writeln(
        '  hollow: no connected device found.\n\n'
        '  Start a simulator or connect a device, then run:\n'
        '    dart run hollow:build\n',
      );
      exit(1);
    }
    stdout.writeln(' $deviceId');
  }

  _printHeader(outDir);

  final process = await Process.start('flutter', [
    'run',
    '--dart-define=HOLLOW_BUILD=true',
    '--device-id', deviceId,
    '--no-pub',
  ]);

  final collected = <String, Map<String, dynamic>>{};
  Timer? idleTimer;

  void onCapture(String name, Map<String, dynamic> json) {
    collected[name] = json;
    final boneCount = (json['bones'] as List?)?.length ?? 0;
    stdout.writeln('    \x1b[32m✓\x1b[0m  ${name.padRight(24)} $boneCount bones');
    idleTimer?.cancel();
    idleTimer = Timer(Duration(milliseconds: timeoutMs), () async {
      await _writeOutput(collected, outDir);
      process.kill();
    });
  }

  process.stdout
      .transform(utf8.decoder)
      .transform(const LineSplitter())
      .listen((line) {
    if (!line.contains(_prefix)) return;
    final start = line.indexOf(_prefix) + _prefix.length;
    final rest = line.substring(start);
    final sep = rest.indexOf(':');
    if (sep < 0) return;
    final name = rest.substring(0, sep);
    final jsonStr = rest.substring(sep + 1);
    try {
      onCapture(name, jsonDecode(jsonStr) as Map<String, dynamic>);
    } catch (_) {}
  });

  process.stderr
      .transform(utf8.decoder)
      .transform(const LineSplitter())
      .listen((line) {
    if (line.contains('Error') || line.contains('error')) {
      stderr.writeln('  \x1b[31m$line\x1b[0m');
    }
  });

  final exitCode = await process.exitCode;

  if (collected.isEmpty) {
    stderr.writeln(
      '\n  hollow: nothing captured.\n\n'
      '  Make sure your Skeleton widgets have `name` and `fixture` set.\n'
      '  Example:\n'
      '    Skeleton(\n'
      '      name: \'blog-card\',\n'
      '      loading: false,\n'
      '      fixture: BlogCard(data: mockData),\n'
      '      child: BlogCard(data: realData),\n'
      '    )\n',
    );
    exit(1);
  }

  exit(exitCode == 0 || exitCode == -15 ? 0 : exitCode);
}

Future<void> _writeOutput(
  Map<String, Map<String, dynamic>> collected,
  String outDir,
) async {
  final dir = Directory(outDir);
  await dir.create(recursive: true);

  stdout.writeln('\n  \x1b[2m${'─' * 50}\x1b[0m');
  stdout.writeln('  \x1b[1mWriting files\x1b[0m\n');

  for (final entry in collected.entries) {
    final name = entry.key;
    final data = entry.value;
    final file = File('${dir.path}/$name.bones.json');
    await file.writeAsString(const JsonEncoder.withIndent('  ').convert(data));
    stdout.writeln('  \x1b[32m→\x1b[0m $name.bones.json');
  }

  final names = collected.keys.toList();
  final registryLines = <String>[
    '// Auto-generated by `dart run hollow:build` — do not edit',
    "import 'package:hollow/hollow.dart';",
    '',
  ];

  for (final name in names) {
    final varName = '_${_toIdentifier(name)}';
    final json = const JsonEncoder().convert(collected[name]);
    registryLines.add('final $varName = SkeletonResult.fromJson($json);');
  }

  registryLines.addAll([
    '',
    'void registerAllBones() {',
    '  HollowRegistry.register({',
  ]);

  for (final name in names) {
    final varName = '_${_toIdentifier(name)}';
    registryLines.add("    '$name': $varName,");
  }

  registryLines.addAll([
    '  });',
    '}',
    '',
  ]);

  final registryFile = File('${dir.path}/bones_registry.dart');
  await registryFile.writeAsString(registryLines.join('\n'));

  final count = names.length;
  stdout.writeln('  \x1b[32m→\x1b[0m bones_registry.dart  '
      '\x1b[2m($count skeleton${count != 1 ? 's' : ''})\x1b[0m');
  stdout.writeln(
    '\n  \x1b[32m\x1b[1m💀 $count skeleton${count != 1 ? 's' : ''} captured.\x1b[0m\n',
  );
  stdout.writeln(
    "  \x1b[2mAdd once to main():\x1b[0m  import '$outDir/bones_registry.dart';\n"
    "  \x1b[2m                      \x1b[0m  registerAllBones();\n",
  );
}

Future<String?> _detectDevice() async {
  try {
    final result = await Process.run('flutter', ['devices', '--machine']);
    final devices = jsonDecode(result.stdout as String) as List<dynamic>;

    final supported = devices
        .cast<Map<String, dynamic>>()
        .where((d) => d['isSupported'] == true)
        .toList();

    for (final priority in ['ios-simulator', 'ios', 'android']) {
      final match = supported.firstWhere(
        (d) => ((d['targetPlatform'] as String?) ?? '').contains(priority),
        orElse: () => {},
      );
      if (match.isNotEmpty) return match['id'] as String?;
    }
    return null;
  } catch (_) {
    return null;
  }
}

void _printHeader(String outDir) {
  stdout.writeln('\n  \x1b[1m💀 hollow build\x1b[0m');
  stdout.writeln('  \x1b[2m${'─' * 50}\x1b[0m');
  stdout.writeln('  \x1b[2moutput\x1b[0m  $outDir\n');
}

void _printHelp() {
  stdout.writeln('''
  hollow build [options]

  Launches your Flutter app in capture mode, snapshots all named Skeleton
  widgets at their rendered positions, and writes bone data to disk.

  Options:
    --device <id>    Flutter device ID      (default: auto-detected)
    --out <dir>      Output directory       (default: lib/bones)
    --timeout <ms>   Idle timeout after last capture (default: 5000)

  Setup:
    1. Wrap your widget:
       Skeleton(
         name: 'blog-card',
         loading: isLoading,
         fixture: BlogCard(data: mockData),
         child: BlogCard(data: realData),
       )

    2. Run: dart run hollow:build

    3. Import once in main():
       import 'bones/bones_registry.dart';
       void main() {
         registerAllBones();
         runApp(MyApp());
       }
''');
}

String? _argValue(List<String> args, String flag) {
  final i = args.indexOf(flag);
  if (i < 0 || i + 1 >= args.length) return null;
  return args[i + 1];
}

String _toIdentifier(String name) =>
    name.replaceAll(RegExp(r'[^a-zA-Z0-9]'), '_');
