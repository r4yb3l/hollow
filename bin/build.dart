import 'dart:async';
import 'dart:convert';
import 'dart:io';

import 'src/ast_scanner.dart';
import 'src/writer.dart';

void main(List<String> args) async {
  if (args.contains('--help') || args.contains('-h')) {
    _printHelp();
    exit(0);
  }

  final deviceId = argValue(args, '-d');
  final outDir = argValue(args, '--out') ?? 'lib/bones';
  final timeoutMs = int.tryParse(argValue(args, '--timeout') ?? '') ?? 5000;
  final keepFile = args.contains('--keep');

  if (deviceId == null) {
    stderr.writeln(
      '\n  hollow: no device specified.\n\n'
      '  Pass a device id with -d:\n'
      '    dart run hollow:build -d <device-id>\n\n'
      '  List available devices with:\n'
      '    flutter devices\n',
    );
    exit(1);
  }

  final packageName = await readPackageName('.');

  _printHeader(outDir, packageName);

  // --- Scan -----------------------------------------------------------------
  stdout.writeln('  \x1b[2mScanning lib/ for Skeleton usages...\x1b[0m');
  final skeletons = await AstHollowScanner.scan('lib', packageName);

  if (skeletons.isEmpty) {
    stderr.writeln(
      '\n  hollow: no Skeleton widgets found.\n\n'
      '  Wrap any widget with Skeleton and provide a name:\n\n'
      '    Skeleton(\n'
      "      name: 'blog-card',\n"
      '      loading: isLoading,\n'
      '      child: BlogCard(title: post.title),\n'
      '    )\n',
    );
    exit(1);
  }

  for (final s in skeletons) {
    stdout.writeln(
      '    \x1b[32m✓\x1b[0m  ${s.name.padRight(24)} ${s.sourceFilePath}',
    );
  }
  stdout.writeln();

  // --- Generate CaptureGallery ----------------------------------------------
  final captureContent = _generateCaptureGallery(skeletons, packageName);
  final captureFile = await _writeCaptureFile(captureContent);

  if (keepFile) {
    stdout.writeln(
      '  \x1b[2mGenerated capture app: ${captureFile.path}\x1b[0m\n',
    );
  }

  // --- Run --------------------------------------------------------------------
  stdout.writeln('  \x1b[2mLaunching capture app on $deviceId...\x1b[0m\n');

  final process = await Process.start('flutter', [
    'run',
    '-t',
    captureFile.path,
    '--dart-define=HOLLOW_BUILD=true',
    '--device-id',
    deviceId,
    '--no-pub',
  ]);

  final collected = <String, Map<String, dynamic>>{};
  Timer? idleTimer;

  void onCapture(String name, Map<String, dynamic> json) {
    collected[name] = json;
    final boneCount = (json['bones'] as List?)?.length ?? 0;
    stdout.writeln(
      '    \x1b[32m✓\x1b[0m  ${name.padRight(24)} $boneCount bones',
    );
    idleTimer?.cancel();
    idleTimer = Timer(Duration(milliseconds: timeoutMs), () async {
      if (collected.isNotEmpty) {
        await _writeAndFinish(collected, outDir);
      }
      process.kill();
    });
  }

  process.stdout
      .transform(const SystemEncoding().decoder)
      .transform(const LineSplitter())
      .listen((line) {
    const prefix = 'HOLLOW_JSON:';
    if (!line.contains(prefix)) return;
    final start = line.indexOf(prefix) + prefix.length;
    final rest = line.substring(start);
    final sep = rest.indexOf(':');
    if (sep < 0) return;
    final name = rest.substring(0, sep);
    final jsonStr = rest.substring(sep + 1);
    try {
      onCapture(name, jsonDecode(jsonStr) as Map<String, dynamic>);
    } catch (_) {}
  });

  process.stderr
      .transform(const SystemEncoding().decoder)
      .transform(const LineSplitter())
      .listen((line) {
    if (line.contains('Error') || line.contains('error')) {
      stderr.writeln('  \x1b[31m$line\x1b[0m');
    }
  });

  final exitCode = await process.exitCode;

  if (!keepFile) {
    try {
      await captureFile.delete();
    } catch (_) {}
  }

  if (collected.isEmpty) {
    stderr.writeln(
      '\n  hollow: nothing captured.\n\n'
      '  Make sure your Skeleton widgets have `name` set.\n',
    );
    exit(exitCode == 0 || exitCode == -15 ? 0 : exitCode);
  }
}

String _generateCaptureGallery(List<DiscoveredSkeleton> skeletons, String packageName) {
  final lines = <String>[];

  lines.addAll([
    '// AUTO-GENERATED by dart run hollow:build — do not edit',
    "import 'package:flutter/material.dart';",
    "import 'package:hollow/hollow.dart';",
  ]);

  // Collect unique imports
  final allImports = <String>{};
  for (final s in skeletons) {
    allImports.addAll(s.packageImports);
  }
  allImports.removeWhere(
    (imp) =>
        imp.contains('package:hollow/') ||
        imp.contains('package:flutter/') ||
        imp.contains("'dart:"),
  );
  for (final imp in allImports.toList()..sort()) {
    lines.add(imp);
  }

  lines.addAll([
    '',
    'void main() {',
    '  runApp(const CaptureApp());',
    '}',
    '',
    'class CaptureApp extends StatelessWidget {',
    '  const CaptureApp({super.key});',
    '',
    '  @override',
    '  Widget build(BuildContext context) {',
    '    return MaterialApp(',
    '      debugShowCheckedModeBanner: false,',
    '      home: Scaffold(',
    '        body: ListView.separated(',
    '          padding: const EdgeInsets.all(16),',
    '          itemCount: ${skeletons.length},',
    '          separatorBuilder: (_, _) => const SizedBox(height: 24),',
    '          itemBuilder: (context, index) {',
    '            final name = _names[index];',
    '            return Skeleton(',
    "              name: name,",
    '              loading: true,',
    '              child: const SizedBox(height: 200, width: double.infinity),',
    '            );',
    '          },',
    '        ),',
    '      ),',
    '    );',
    '  }',
    '}',
    '',
    'const _names = [',
  ]);

  for (final s in skeletons) {
    lines.add("  '${s.name}',");
  }

  lines.addAll([
    '];',
    '',
  ]);

  return lines.join('\n');
}

Future<File> _writeCaptureFile(String content) async {
  final dir = Directory('lib');
  if (!await dir.exists()) {
    await dir.create();
  }
  final file = File('lib/hollow_capture_main.dart');
  await file.writeAsString(content);
  return file;
}

Future<void> _writeAndFinish(
  Map<String, Map<String, dynamic>> collected,
  String outDir,
) async {
  final existing = await readExistingBones(outDir);
  await writeOutput(collected, existing, outDir);

  // Clean up capture file
  try {
    final captureFile = File('lib/hollow_capture_main.dart');
    if (await captureFile.exists()) {
      await captureFile.delete();
    }
  } catch (_) {}
}

String? argValue(List<String> args, String flag) {
  final i = args.indexOf(flag);
  if (i < 0 || i + 1 >= args.length) return null;
  return args[i + 1];
}

Future<String> readPackageName(String dir) async {
  final file = File('$dir/pubspec.yaml');
  if (!await file.exists()) return '';
  final content = await file.readAsString();
  final match = RegExp(r'^name:\s*(.+)$', multiLine: true).firstMatch(content);
  return match?.group(1)?.trim() ?? '';
}

void _printHeader(String outDir, String packageName) {
  stdout.writeln('\n  \x1b[1m💀 hollow build\x1b[0m');
  stdout.writeln('  \x1b[2m${'─' * 50}\x1b[0m');
  stdout.writeln('  \x1b[2mdevice\x1b[0m  $packageName');
  stdout.writeln('  \x1b[2moutput\x1b[0m   $outDir\n');
}

void _printHelp() {
  stdout.writeln('''
  hollow build [options]

  Scans lib/ for Skeleton widgets, generates a capture gallery app,
  and runs it on a device to capture pixel-perfect bone data.

  Options:
    -d <id>          Flutter device ID      (required, see: flutter devices)
    --out <dir>       Output directory       (default: lib/bones)
    --timeout <ms>    Idle timeout after last capture (default: 5000)
    --force          Recapture already-captured skeletons
    --keep           Keep generated capture app for debugging

  Setup:
    1. Wrap any widget with Skeleton and provide a name:
         Skeleton(
           name: 'blog-card',
           loading: isLoading,
           child: BlogCard(title: post.title),
         )

    2. Run: dart run hollow:build -d <device>

    3. Import once in main():
         import 'bones/bones_registry.dart';
         void main() {
           registerAllBones();
           // ...
         }
''');
}
